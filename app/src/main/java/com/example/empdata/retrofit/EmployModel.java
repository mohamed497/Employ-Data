package com.example.empdata.retrofit;

public class EmployModel {

    String name;
    String  age;
    String  phone;
    String  salary;
    String id;

    public EmployModel(String name, String  age, String  phone, String  salary, String  id) {
        this.name = name;
        this.age = age;
        this.phone = phone;
        this.salary = salary;
        this.id = id;
    }
}
